import math

a = 2.3

print(math.ceil(a)) # ближайшее целое число, которое больше чем x
print(math.floor(a)) # ближайшее целое число, которое меньше чем x

b = -10
c = 4

print(math.fabs(b)) # модуль 
print(math.factorial(c)) # факториал

d = 4
print(math.exp(d)) # e^x
print(math.log(8, 2)) # логарифм

print(math.pow(2, 4)) # степень
print(math.sqrt(36)) # квадратный корень