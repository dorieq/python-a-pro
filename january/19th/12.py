import math

a = 2.3

print(math.ceil(a)) # ближайшее число к x, которое больше чем х
print(math.floor(a)) # ближайшее число к x, которое меньше чем х

b = -10
c = 4

print(math.fabs(b)) # модуль
print(math.factorial(c)) # факториал

print(math.pow(2, 4)) # возведение в степень
print(math.sqrt(36)) # квадратный корень